    cqss(sqssCH2GSG) = (A_F_F - (CH2GSG_HCCO) * cqss(sqssHCCO) - (a_bi_e) &
                 * cqss(sqssC2H3)) / (a_bh_f)  

    cqss(sqssCH2) = (A_E_E - (a_bz_e) * cqss(sqssC2H3) - (a_ay_b) &
                 * cqss(sqssCH2GSG)) / (a_ax_a)  

    cqss(sqssCH3O2) = (A_D_D - (a_aq_a) * cqss(sqssC2H3) - (a_ap_b) &
                 * cqss(sqssCH2GSG) - (a_ao_a) * cqss(sqssCH2)) &
                 / (a_an_d)  

    cqss(sqssCH3O) = (A_C_C - (a_ag_b) * cqss(sqssCH2GSG) - (a_ae_b) &
                 * cqss(sqssCH3O2)) / (a_ad_b)  

    cqss(sqssCH2OH) = (A_B_B - (a_x_b) * cqss(sqssCH2GSG) - (a_v_b) &
                 * cqss(sqssCH3O2) - (a_u_b) * cqss(sqssCH3O)) &
                 / (a_t_b)  

    cqss(sqssCH3OH) = A_A_A - (a_o_a) * cqss(sqssCH2GSG) - (a_m_a) &
                 * cqss(sqssCH3O2) - (CH3OH_CH3O) * cqss(sqssCH3O) &
                 - (CH3OH_CH2OH) * cqss(sqssCH2OH)  

    cqss(sqssHCO) = HCO_ct - (HCO_C2H3) * cqss(sqssC2H3) - (HCO_CH2GSG) &
                 * cqss(sqssCH2GSG) - (HCO_CH2) * cqss(sqssCH2) &
                 - (HCO_CH3O2) * cqss(sqssCH3O2)  

    C_denom = tiny(1.0_pr) + ( 0.0_pr &
             + k(r108f)* c(sOH) &
             + k(r109f)* c(sO2)   )

    C_num = ( 0.0_pr &
             + k(r108b)* c(sCO) * c(sH) &
             + k(r109b)* c(sCO) * c(sO)   )

    C_ct = C_num / C_denom

    cqss(sqssC) = C_ct  

    C2H5_denom = tiny(1.0_pr) + ( 0.0_pr &
             + k(r111f)* c(sH) &
             + k(r120f)* c(sCH3) &
             + k(r122f)* c(sH) &
             + k(r123f)* c(sO2) &
             + k(r124f)* c(sO2) &
             + k(r112b)* c(sH2) &
             + k(r113b)* c(sOH) &
             + k(r114b)* c(sH2O) &
             + k(r115b)* c(sHO2) &
             + k(r116b)* c(sCH4) &
             + k(r117b)* c(sH2O2) &
             + k(r118b)&
             + k(r121b)* c(sH)   )

    C2H5_num = ( 0.0_pr &
             + k(r118f)* c(sC2H4) * c(sH) &
             + k(r121f) *c(sCH3)** 2.0_pr&
             + k(r120b)* c(sC2H4) * c(sCH4) &
             + k(r122b)* c(sC2H4) * c(sH2) &
             + k(r123b)* c(sC2H4) * c(sHO2) &
             + k(r124b)* c(sC2H4) * c(sHO2)   )

    C2H5_ct = C2H5_num / C2H5_denom

    C2H5_C2H6 = - ( 0.0_pr &
          + k(r112f) * c(sH) &
          + k(r113f) * c(sO) &
          + k(r114f) * c(sOH) &
          + k(r115f) * c(sO2) &
          + k(r116f) * c(sCH3) &
          + k(r117f) * c(sHO2) &
          + k(r111b)  ) / C2H5_denom

    C2H6_denom = tiny(1.0_pr) + ( 0.0_pr &
             + k(r112f)* c(sH) &
             + k(r113f)* c(sO) &
             + k(r114f)* c(sOH) &
             + k(r115f)* c(sO2) &
             + k(r116f)* c(sCH3) &
             + k(r117f)* c(sHO2) &
             + k(r110b)&
             + k(r111b)  )

    C2H6_num = ( 0.0_pr &
             + k(r110f) *c(sCH3)** 2.0_pr  )

    C2H6_ct = C2H6_num / C2H6_denom

    C2H6_C2H5 = - ( 0.0_pr &
          + k(r111f) * c(sH) &
          + k(r112b) * c(sH2) &
          + k(r113b) * c(sOH) &
          + k(r114b) * c(sH2O) &
          + k(r115b) * c(sHO2) &
          + k(r116b) * c(sCH4) &
          + k(r117b) * c(sH2O2)  ) / C2H6_denom

    b_c_a =  (1.0_pr) - (C2H6_C2H5) * (C2H5_C2H6)
    B_A_A =  (C2H5_ct) - (C2H6_ct) * (C2H5_C2H6)

    cqss(sqssC2H5) = ( B_A_A ) / ( b_c_a )  

    cqss(sqssC2H6) = C2H6_ct - (C2H6_C2H5) * cqss(sqssC2H5)  

    cqss = max(cqss, tiny(1.0_pr))
    cqss = min(cqss, 1e03_pr)

    ! Necessary for very low concentration variations
    ! Needs to be integrated only for Cantera
    do index=1,nqss
      if (cqss(index) /= cqss(index)) then
        cqss(index) = tiny(1.0_pr)
      end if
    end do

    return
  end subroutine get_qss


  ! ----------------------------------------------- !
  ! Mass fractions to concentrations                !
  ! ----------------------------------------------- !
  subroutine y2c(y, W_sp, P, T, c)
    implicit none

    real(pr),dimension(nspec) :: W_sp
    real(pr),dimension(nspec) :: c, y
    real(pr) :: rho, P, T, inv_W_g

    integer :: k
    
    ! Gas molecular weight inverse
    inv_W_g = 0.0_pr
    do k =1, nspec
      inv_W_g = inv_W_g + y(k) / W_sp(k)
    end do

    ! Gas density
    rho = P / (Rcst * inv_W_g * T)

    ! Conversion
    c = y * rho / W_sp

    ! Concentrations clipping
    !do  k = 1, nspec
    !   if (c(k)<1.0e-20_pr) c(k) = 0.0_pr
    !end do

        return
    end subroutine y2c

end module mod_customkinetics
! ----------------------------------------------- !
! Cantera routine                                 !
! ----------------------------------------------- !
subroutine customkinetics(P, T, y, wdot)

  use mod_customkinetics
  implicit none

  real(pr), dimension(nspec) :: y, c, wdot, cdot
  real(pr), dimension(nqss) :: cqss
  real(pr), dimension(nreac + nreac_reverse) :: w,k
  real(pr), dimension(nTB + nFO) :: M
  real(pr) :: P, T, rho

  ! Convert to concentrations
  call y2c(y, W_sp, P, T, c)

  ! Evaluate QSS concentrations and reaction rates
  call get_thirdbodies(M,c)

  call get_rate_coefficients(k, M, T, P)
  call get_qss(cqss, c, k, M)

  call get_reaction_rates(w, k, M, c, cqss)

  ! Evaluate production rates
  call get_production_rates(wdot,w)


  return
end subroutine customkinetics

